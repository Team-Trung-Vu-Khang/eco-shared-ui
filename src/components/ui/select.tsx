export type AutoCompleteOption = {
  label: string;
  value: string;
  keywords?: string[];
};

interface AutoCompleteSelectProps {
  options: AutoCompleteOption[];
  value?: string;
  onChange: (value: string) => void;
  placeholder?: string;
  searchPlaceholder?: string;
  emptyText?: string;
  disabled?: boolean;
  clearable?: boolean;
}

export function AutoCompleteSelect({
  options,
  value,
  onChange,
  placeholder = "Chọn...",
  searchPlaceholder = "Tìm kiếm...",
  emptyText = "Không có dữ liệu",
  disabled,
  clearable = false,
}: AutoCompleteSelectProps) {
  const [open, setOpen] = React.useState(false);

  // Vietnamese accent-insensitive filter
  const commandFilter = (
    value: string,
    search: string,
    keywords?: string[],
  ) => {
    const extendValue = (
      value +
      " " +
      (keywords?.join(" ") || "")
    ).toLowerCase();
    if (extendValue.includes(search.toLowerCase())) return 1;

    const normalize = (str: string) =>
      str
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .replace(/đ/g, "d")
        .replace(/Đ/g, "D")
        .toLowerCase();

    const normalizedValue = normalize(extendValue);
    const normalizedSearch = normalize(search);

    if (normalizedValue.includes(normalizedSearch)) return 1;
    return 0;
  };

  const selectedOption = options.find((opt) => opt.value === value);

  const handleClear = (e: React.MouseEvent) => {
    e.stopPropagation();
    onChange("");
  };

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          disabled={disabled}
          className={cn(
            "w-full justify-between px-3 py-2 h-9 group",
            "text-sm font-normal shadow-sm border-input bg-transparent",
            !value && "text-muted-foreground",
            disabled && "cursor-not-allowed opacity-50",
          )}
        >
          <span className="truncate">
            {selectedOption ? selectedOption.label : placeholder}
          </span>
          <div className="flex items-center">
            {clearable && value && !disabled && (
              <div
                role="button"
                onClick={handleClear}
                className="mr-2 rounded-full p-0.5 hover:bg-muted opacity-60 hover:opacity-100 transition-all cursor-pointer hidden group-hover:block"
              >
                <X className="h-3 w-3" />
              </div>
            )}
            <ChevronsUpDown className="h-4 w-4 shrink-0 opacity-50" />
          </div>
        </Button>
      </PopoverTrigger>
      <PopoverContent
        className="w-[--radix-popover-trigger-width] p-0"
        align="start"
      >
        <Command filter={commandFilter}>
          <CommandInput placeholder={searchPlaceholder} />
          <CommandList>
            <CommandEmpty>{emptyText}</CommandEmpty>
            <CommandGroup>
              {options.map((option) => (
                <CommandItem
                  key={option.value}
                  value={option.label}
                  keywords={[option.value, ...(option.keywords || [])]}
                  onSelect={() => {
                    onChange(option.value === value ? "" : option.value);
                    setOpen(false);
                  }}
                >
                  <Check
                    className={cn(
                      "mr-2 h-4 w-4",
                      value === option.value ? "opacity-100" : "opacity-0",
                    )}
                  />
                  {option.label}
                </CommandItem>
              ))}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  );
}
